#  programa que leia dois numeros. Imprima a divisão inteira
#   do primeiro pelo segundo, assim como o resto da divisão. Utilize
#     apenas os operadores de coma e subtração. Lembre-se de que podemos
#       retirar o divisor do dividendo

n = int (input("Digite um numero: "))
z = int (input("Digite um numero: "))
x=1
while x <= z:
    print(n / x)
    x = x+1